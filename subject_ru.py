# -*- coding: utf-8 -*-
"""
Формы школьных предметов после предлога «по»: по математике, по русскому языку, …
Для неизвестных названий — нейтральная конструкция без ошибочного автосклонения.
"""

_SUBJECT_PO = {
    "Математика": "математике",
    "Алгебра": "алгебре",
    "Геометрия": "геометрии",
    "Русский язык": "русскому языку",
    "Литература": "литературе",
    "Физика": "физике",
    "Химия": "химии",
    "Биология": "биологии",
    "История": "истории",
    "Обществознание": "обществознанию",
    "География": "географии",
    "Английский язык": "английскому языку",
    "Информатика": "информатике",
    "Искусство": "искусству",
    "Музыка": "музыке",
    "ОБЖ": "ОБЖ",
    "Астрономия": "астрономии",
    "Экология": "экологии",
}


def subject_po(name: str) -> str:
    """
    Краткая форма для фраз вида «вопрос по …», «тема по …».
    Примеры: subject_po('География') -> 'географии'
    """
    key = (name or "").strip()
    if not key:
        return "школьному предмету"
    if key in _SUBJECT_PO:
        return _SUBJECT_PO[key]
    # Пробуем совпадение без учёта регистра первой буквы
    for canon, form in _SUBJECT_PO.items():
        if canon.lower() == key.lower():
            return form
    return f"предмету «{key}»"


def decline_subject(subject: str) -> str:
    """
    Склонение названия предмета для конструкций «по биологии», «по русскому языку».
    Алиас subject_po — используй decline_subject в промптах и шаблонах для ясности.
    """
    return subject_po(subject)

